                ItemObject.ItemTypeEnum.Goods => InventoryScreenHelper.InventoryItemType.Goods,
                ItemObject.ItemTypeEnum.HeadArmor => InventoryScreenHelper.InventoryItemType.HeadArmor,
                ItemObject.ItemTypeEnum.BodyArmor => InventoryScreenHelper.InventoryItemType.BodyArmor,
                ItemObject.ItemTypeEnum.LegArmor => InventoryScreenHelper.InventoryItemType.LegArmor,
                ItemObject.ItemTypeEnum.HandArmor => InventoryScreenHelper.InventoryItemType.HandArmor,
