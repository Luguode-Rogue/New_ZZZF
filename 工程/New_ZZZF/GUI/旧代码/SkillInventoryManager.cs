            if (item == null) return InventoryScreenHelper.InventoryItemType.None;
            return item.ItemType switch
            {
                ItemObject.ItemTypeEnum.Horse => InventoryScreenHelper.InventoryItemType.Horse,
                ItemObject.ItemTypeEnum.OneHandedWeapon => InventoryScreenHelper.InventoryItemType.Weapon,
                ItemObject.ItemTypeEnum.TwoHandedWeapon => InventoryScreenHelper.InventoryItemType.Weapon,
                ItemObject.ItemTypeEnum.Polearm => InventoryScreenHelper.InventoryItemType.Weapon,
                ItemObject.ItemTypeEnum.Arrows => InventoryScreenHelper.InventoryItemType.Weapon,
                ItemObject.ItemTypeEnum.Bolts => InventoryScreenHelper.InventoryItemType.Weapon,
                ItemObject.ItemTypeEnum.Shield => InventoryScreenHelper.InventoryItemType.Shield,
                ItemObject.ItemTypeEnum.Bow => InventoryScreenHelper.InventoryItemType.Weapon,
                ItemObject.ItemTypeEnum.Crossbow => InventoryScreenHelper.InventoryItemType.Weapon,
